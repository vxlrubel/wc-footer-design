            <h3>Description:</h3>
            <p>
            Transform your website's footer into a sleek and modern design reminiscent of an e-commerce app with 'WC Footer Design.' This powerful WordPress plugin empowers you to create visually appealing and functional footers that captivate your visitors. Customize the layout, colors, and content effortlessly, enhancing user experience and giving your website a professional edge. Elevate your website's footer to match the standards of top e-commerce platforms with 'WC Footer Design.'
            </p>

            <h3>Documentation:</h3>
            <h4 class="fz-15">Installation</h4>
            <p>01. You need to download the plugin. <a href="https://github.com/vxlrubel/wc-footer-design/archive/refs/heads/main.zip" target="_blank">Download</a></p>
            <p>02. Upload the plugin from dashboard <span class="strong">Plugins &gt; Add New &gt; Upload Plugin</span></p>
            <p>03. Install and active after uploading the plugin file.</p>

            <h4 class="fz-15">Use Case</h4>
            <p>01. Activate the plugin.</p>
            <p>02. Goto <span class="strong">Appearance &gt; Menus </span>From WordPress Dashboard</p>
            <p>03. Create a new menu with any name <span class="strong">(ex: App Menu)</span>.</p>
            <p>04. Add items into the menu whice you already created.</p>
            <p>05. You will see the new menu location called <span class="strong">App Menu</span> under the <span class="strong">Menu Setting</span>.</p>
            <p>06. Checked the <span class="strong">App Menu</span> after add the menu items and click <span class="strong">save menu</span>.</p>
            <p>07. Congratulation! You are successfully create a app menu. You are able to see the app menu from front-end of your website.</p>
